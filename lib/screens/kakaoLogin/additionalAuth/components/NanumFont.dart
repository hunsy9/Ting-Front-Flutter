import 'package:flutter/material.dart';

TextStyle nanumFontBold(double fontSize) {
  return TextStyle(
    fontFamily: 'nanumsquareround',
    fontSize: fontSize,
    fontWeight: FontWeight.bold,
    letterSpacing: 1.0,
  );
}

TextStyle nanumFontNormal(double fontSize) {
  return TextStyle(
    fontFamily: 'nanumsquareround',
    fontSize: fontSize,
    letterSpacing: 1.0,
  );
}